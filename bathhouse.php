        <p class="description__paragraph">На Руси культ русской бани существует очень и очень давно. Традиционную русскую парную готовят тщательно: моют пол, открывают дверь – парная должна быть чистой и проветренной, чтобы жар был легким и мягким. В русской бане при средней температуре 60-70 градусов, влажность составляет 80 %. Для создания духа парной используют настои трав: мята, эвкалипт, пихтовое и хвойное масло. Что очень полезно для легких и бронхов.</p>
        <p class="description__paragraph">Париться в русской бане принято сидя или лежа. Голову принято накрывать полотенцем или специальной шапочкой, чтобы волосы не пересушились и не стали ломкими. Первый заход в парилку разогревающий, он не более пяти минут. В первое посещение сходит самый грязный и едкий пот. В последующие разы можно сидеть в парной до первых признаков усталости. Банные гурманы посещают парную за два часа, раз пять, но неподготовленному человеку за это время хватит и трех. После каждого посещения парной можно принять холодный душ, или скупаться в деревянной катке, наполненной холодной водой. Контрастные процедуры очень полезны для кожи и для общего тонуса организма. Особенно полезно это в зимнее время, когда необходимо поднять иммунитет и сопротивляемость к простудным болезням.</p>
